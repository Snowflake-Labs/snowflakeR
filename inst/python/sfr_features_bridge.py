def _to_json_tempfile(pdf):
    """Write a pandas DataFrame to a temp JSON file and return the path.

    Avoids the ``basic_string::substr`` C++ crash in reticulate/rpy2 that
    occurs when large dicts cross the Python <-> R bridge in Workspace
    Notebooks.  The R side reads and parses the file with jsonlite.
    """
    import json
    import tempfile

    d = _pandas_to_r_dict(pdf)
    tmp = tempfile.NamedTemporaryFile(
        mode="w", suffix=".json", delete=False, prefix="sfr_features_"
    )
    json.dump(d, tmp)
    tmp.close()
    return tmp.name


def _pandas_to_r_dict(pdf):
    """Convert a pandas DataFrame to a column-oriented dict with native Python
    types via Series.tolist().  This avoids NumPy ABI issues with reticulate
    and is more efficient than the previous JSON round-trip approach.

    Datetime columns are converted to ISO-format strings to avoid ugly
    POSIX timestamp objects in R output.

    NOTE: For very large datasets, an Apache Arrow IPC transfer (pyarrow on
    the Python side, arrow R package on the R side) would be significantly
    faster.  See TODO.md for the roadmap item.
    """
    import datetime

    _NA = "NA_SENTINEL_"
    clean_cols = [c.strip('"') for c in pdf.columns]
    pdf.columns = clean_cols
    cols = list(clean_cols)
    nrows = len(pdf)

    if nrows == 0:
        return {"columns": cols, "data": {c: [] for c in cols}, "nrows": 0}

    data = {}
    for col in cols:
        na_mask = pdf[col].isna()
        vals = pdf[col].tolist()

        # Convert datetime objects to ISO strings
        if vals and isinstance(vals[0], (datetime.datetime, datetime.date)):
            vals = [v.isoformat() if isinstance(v, (datetime.datetime, datetime.date)) else v for v in vals]

        if na_mask.any():
            data[col] = [_NA if is_na else (_NA if v is None else v)
                         for v, is_na in zip(vals, na_mask)]
        else:
            data[col] = [_NA if v is None else v for v in vals]

    return {"columns": cols, "data": data, "nrows": nrows}


_fs_cache: Dict[tuple, Any] = {}


def _get_feature_store(
    session,
    database: str,
    schema: str,
    default_warehouse: str,
    creation_mode: str = "FAIL_IF_NOT_EXIST",
):
    """
    Get or create a FeatureStore instance, cached by (db, schema, warehouse, mode).

    Args:
        session: Active Snowpark Session.
        database: Database containing the Feature Store schema.
        schema: Schema name for the Feature Store.
        default_warehouse: Default warehouse for compute.
        creation_mode: "CREATE_IF_NOT_EXIST" or "FAIL_IF_NOT_EXIST".

    Returns:
        FeatureStore instance.
    """
    cache_key = (database, schema, default_warehouse, creation_mode)
    if cache_key in _fs_cache:
        return _fs_cache[cache_key]

    from snowflake.ml.feature_store import FeatureStore, CreationMode

    mode_map = {
        "CREATE_IF_NOT_EXIST": CreationMode.CREATE_IF_NOT_EXIST,
        "FAIL_IF_NOT_EXIST": CreationMode.FAIL_IF_NOT_EXIST,
    }
    mode = mode_map.get(creation_mode, CreationMode.FAIL_IF_NOT_EXIST)

    fs = FeatureStore(
        session=session,
        database=database,
        name=schema,
        default_warehouse=default_warehouse,
        creation_mode=mode,
    )
    _fs_cache[cache_key] = fs
    return fs


def _make_entity(name: str, join_keys: List[str], desc: Optional[str] = None):
    """Create a local (draft) Entity object."""
    from snowflake.ml.feature_store import Entity
    return Entity(name=name, join_keys=join_keys, desc=desc or "")


def _make_feature_view(
    session,
    name: str,
    entities: list,
    sql: str,
    refresh_freq: Optional[str] = None,
    warehouse: Optional[str] = None,
    timestamp_col: Optional[str] = None,
    desc: Optional[str] = None,
    feature_granularity: Optional[str] = None,
    features: Optional[list] = None,
    initialize: Optional[str] = None,
    refresh_mode: Optional[str] = None,
    cluster_by: Optional[List[str]] = None,
    online_config_dict: Optional[Dict[str, Any]] = None,
):
    """Create a local (draft) FeatureView object."""
    from snowflake.ml.feature_store import FeatureView

    feature_df = session.sql(sql)

    fv_kwargs: Dict[str, Any] = {
        "name": name,
        "entities": entities,
        "feature_df": feature_df,
        "refresh_freq": refresh_freq,
        "timestamp_col": timestamp_col,
        "desc": desc or "",
        "warehouse": warehouse,
    }
    if feature_granularity is not None:
        fv_kwargs["feature_granularity"] = feature_granularity
    if features is not None:
        fv_kwargs["features"] = features
    if initialize is not None:
        fv_kwargs["initialize"] = initialize
    if refresh_mode is not None:
        fv_kwargs["refresh_mode"] = refresh_mode
    if cluster_by is not None:
        fv_kwargs["cluster_by"] = list(cluster_by)
    if online_config_dict is not None:
        _requires((1, 18, 0), "Online feature serving (online_config on FV)")
        from snowflake.ml.feature_store.feature_view import OnlineConfig
        fv_kwargs["online_config"] = OnlineConfig(
            enable=online_config_dict.get("enable", True),
            target_lag=online_config_dict.get("target_lag"),
        )

    return FeatureView(**fv_kwargs)


