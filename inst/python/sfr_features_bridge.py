def generate_training_set(
    session,
    spine_sql: str,
    feature_view_refs: List[Dict[str, str]],
    spine_timestamp_col: Optional[str] = None,
    spine_label_cols: Optional[List[str]] = None,
    save_as: Optional[str] = None,
    database: Optional[str] = None,
    schema: Optional[str] = None,
    warehouse: Optional[str] = None,
    creation_mode: str = "FAIL_IF_NOT_EXIST",
    exclude_columns: Optional[List[str]] = None,
    include_feature_view_timestamp_col: bool = False,
    auto_prefix: bool = False,
    join_method: Optional[str] = None,
) -> Any:
    """Generate a training set by joining spine data with Feature Views."""
    db = database or session.get_current_database().replace('"', '')
    sc = schema or session.get_current_schema().replace('"', '')
    wh = warehouse or session.get_current_warehouse().replace('"', '')

    fs = _get_feature_store(session, db, sc, wh, creation_mode)
    spine_df = session.sql(spine_sql)
    fvs = [fs.get_feature_view(ref["name"], ref["version"]) for ref in feature_view_refs]

    kwargs: Dict[str, Any] = {}
    if spine_timestamp_col:
        kwargs["spine_timestamp_col"] = spine_timestamp_col
    if spine_label_cols:
        kwargs["spine_label_cols"] = spine_label_cols
    if save_as:
        kwargs["save_as"] = save_as
    if exclude_columns:
        kwargs["exclude_columns"] = list(exclude_columns)
    if include_feature_view_timestamp_col:
        kwargs["include_feature_view_timestamp_col"] = True
    if auto_prefix:
        kwargs["auto_prefix"] = True
    if join_method:
        kwargs["join_method"] = join_method

    result_df = fs.generate_training_set(
        spine_df=spine_df,
        features=fvs,
        **kwargs,
    )

    return _to_json_tempfile(result_df.to_pandas())


# Module-level cache for Dataset objects so sfr_log_model can retrieve
# a Snowpark DataFrame for lineage without re-opening the Dataset.
_DATASET_CACHE: Dict[str, Any] = {}


def generate_dataset(
    session,
    name: str,
    spine_sql: str,
    feature_view_refs: List[Dict[str, str]],
    version: Optional[str] = None,
    spine_timestamp_col: Optional[str] = None,
    spine_label_cols: Optional[List[str]] = None,
    desc: str = "",
    database: Optional[str] = None,
    schema: Optional[str] = None,
    warehouse: Optional[str] = None,
    creation_mode: str = "FAIL_IF_NOT_EXIST",
    exclude_columns: Optional[List[str]] = None,
    include_feature_view_timestamp_col: bool = False,
    auto_prefix: bool = False,
    output_type: Optional[str] = None,
    join_method: Optional[str] = None,
) -> Dict[str, Any]:
    """
    Generate an immutable, versioned Dataset from Feature Views.

    Unlike generate_training_set (which returns a transient DataFrame),
    this creates a Snowflake ML Dataset object that participates in
    ML Lineage: Feature View -> Dataset -> Model.

    The Dataset object is cached so that sfr_log_model can later retrieve
    a Snowpark DataFrame backed by it for sample_input_data.

    Returns:
        Dict with keys: json_path, dataset_name, dataset_version.
    """
    db = database or session.get_current_database().replace('"', '')
    sc = schema or session.get_current_schema().replace('"', '')
    wh = warehouse or session.get_current_warehouse().replace('"', '')

    fs = _get_feature_store(session, db, sc, wh, creation_mode)

    spine_df = session.sql(spine_sql)
    fvs = [
        fs.get_feature_view(ref["name"], ref["version"])
        for ref in feature_view_refs
    ]

    kwargs: Dict[str, Any] = {}
    if spine_timestamp_col:
        kwargs["spine_timestamp_col"] = spine_timestamp_col
    if spine_label_cols:
        kwargs["spine_label_cols"] = spine_label_cols
    if desc:
        kwargs["desc"] = desc
    if exclude_columns:
        kwargs["exclude_columns"] = list(exclude_columns)
    if include_feature_view_timestamp_col:
        kwargs["include_feature_view_timestamp_col"] = True
    if auto_prefix:
        kwargs["auto_prefix"] = True
    if output_type:
        kwargs["output_type"] = output_type
    if join_method:
        kwargs["join_method"] = join_method

    try:
        ds = fs.generate_dataset(
            name=name,
            spine_df=spine_df,
            features=fvs,
            version=version,
            **kwargs,
        )
    except Exception as e:
        if "already exists" in str(e):
            from snowflake.ml.dataset import load_dataset
            actual_ver = version or name
            fqn = f"{db}.{sc}.{name}"
            print(f"[snowflakeR] Dataset {fqn}:{actual_ver} "
                  "already exists, loading it.")
            ds = load_dataset(session, fqn, actual_ver)
        else:
            raise

    if ds.selected_version:
        actual_version = str(ds.selected_version.name)
    else:
        actual_version = version or name
    cache_key = f"{name}:{actual_version}"
    _DATASET_CACHE[cache_key] = ds
    print(f"[snowflakeR] Dataset cached: {cache_key} "
          f"(type={type(ds).__name__})")

    # Use Snowpark DataFrame path instead of ds.read.to_pandas() to
    # avoid Ray trying to install rpy2 on worker nodes without R.
    # TODO: Add optional Parquet path via ds.read.files() + fsspec
    # for large datasets. See internal/devops/TODO.md.
    pdf = ds.read.to_snowpark_dataframe().to_pandas()
    json_path = _to_json_tempfile(pdf)

    return {
        "json_path": json_path,
        "dataset_name": name,
        "dataset_version": actual_version,
    }


def get_cached_dataset(name: str, version: str):
    """Retrieve a cached Dataset object for use as sample_input_data."""
    cache_key = f"{name}:{version}"
    return _DATASET_CACHE.get(cache_key)


def retrieve_features(
    session,
    spine_sql: str,
    feature_view_refs: List[Dict[str, str]],
    spine_timestamp_col: Optional[str] = None,
    database: Optional[str] = None,
    schema: Optional[str] = None,
    warehouse: Optional[str] = None,
    creation_mode: str = "FAIL_IF_NOT_EXIST",
    exclude_columns: Optional[List[str]] = None,
    include_feature_view_timestamp_col: bool = False,
    auto_prefix: bool = False,
    join_method: Optional[str] = None,
) -> Any:
    """
    Retrieve feature values for inference.

    Without spine_timestamp_col this performs a simple LEFT JOIN on entity
    keys and returns the current feature values.  When spine_timestamp_col
    is provided, point-in-time correct values are returned instead.

    Args:
        session: Active Snowpark Session.
        spine_sql: SQL for entity key values.
        feature_view_refs: List of dicts with "name" and "version" keys.
        spine_timestamp_col: Optional timestamp column for PIT lookup.
        database: Feature Store database.
        schema: Feature Store schema.
        warehouse: Default warehouse.

    Returns:
        pandas DataFrame with feature values.
    """
    db = database or session.get_current_database().replace('"', '')
    sc = schema or session.get_current_schema().replace('"', '')
    wh = warehouse or session.get_current_warehouse().replace('"', '')

    fs = _get_feature_store(session, db, sc, wh, creation_mode)

    spine_df = session.sql(spine_sql)
    fvs = [fs.get_feature_view(ref["name"], ref["version"]) for ref in feature_view_refs]

    kwargs: Dict[str, Any] = {}
    if spine_timestamp_col:
        kwargs["spine_timestamp_col"] = spine_timestamp_col
    if exclude_columns:
        kwargs["exclude_columns"] = list(exclude_columns)
    if include_feature_view_timestamp_col:
        kwargs["include_feature_view_timestamp_col"] = True
    if auto_prefix:
        kwargs["auto_prefix"] = True
    if join_method:
        kwargs["join_method"] = join_method

    result_df = fs.retrieve_feature_values(
        spine_df=spine_df,
        features=fvs,
        **kwargs,
    )

    return _to_json_tempfile(result_df.to_pandas())


