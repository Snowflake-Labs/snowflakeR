"""
Snowflake Feature Store Bridge
===============================

Python backend for snowflakeR::R/features.R.

This module wraps the snowflake.ml.feature_store Python API, providing
functions callable from R via reticulate. Each function receives a Snowpark
session object (from the sfr_connection) and performs Feature Store operations.

Key design decisions:
  - All pandas DataFrames returned to R are converted via JSON round-trip
    (pandas_to_r_dict) to avoid NumPy 1.x/2.x ABI issues with reticulate.
  - The FeatureStore object is instantiated per-call or cached in the R-side
    connection object to avoid stale references.
  - FeatureView source data is passed as SQL strings from R (generated by
    dbplyr or written manually), then wrapped in session.sql().
"""

import importlib.metadata
from typing import Dict, List, Optional, Any

try:
    _ML_VERSION_STR = importlib.metadata.version("snowflake-ml-python")
    _ML_VERSION = tuple(int(x) for x in _ML_VERSION_STR.split(".")[:3])
except Exception:
    _ML_VERSION_STR = "unknown"
    _ML_VERSION = (0, 0, 0)


def _requires(min_version, feature_name):
    """Raise RuntimeError if the installed snowflake-ml-python is too old."""
    if _ML_VERSION < min_version:
        need = ".".join(str(x) for x in min_version)
        raise RuntimeError(
            f"{feature_name} requires snowflake-ml-python >= {need}, "
            f"you have {_ML_VERSION_STR}. "
            f"Upgrade: pip install 'snowflake-ml-python>={need}'"
        )


